  @override
  Widget build(BuildContext context) {
    return Scaffold(
      body: Column(
        children: [
          TabBar(
            controller: _tabController,
            tabs: const [
              Tab(text: 'Client'),
              Tab(text: 'SubsiDiary'),
              Tab(text: "Contacts"),
